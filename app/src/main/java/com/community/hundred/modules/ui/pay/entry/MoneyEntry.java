package com.community.hundred.modules.ui.pay.entry;

public class MoneyEntry {

    /**
     * money : 0.00
     * zong : 0
     * ktxian : 0
     */

    private String money;
    private String zong;
    private String ktxian;

    public String getMoney() {
        return money;
    }

    public void setMoney(String money) {
        this.money = money;
    }

    public String getZong() {
        return zong;
    }

    public void setZong(String zong) {
        this.zong = zong;
    }

    public String getKtxian() {
        return ktxian;
    }

    public void setKtxian(String ktxian) {
        this.ktxian = ktxian;
    }
}
