package com.community.hundred.modules.ui.livebroadcast.entry;

public class LiveBroadCastEntry  {

    private String id;

    private int icon;

    private String name;

    private String list;

    private String clist;

    public String getList() {
        return list;
    }

    public void setList(String list) {
        this.list = list;
    }

    public String getClist() {
        return clist;
    }

    public void setClist(String clist) {
        this.clist = clist;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public int getIcon() {
        return icon;
    }

    public void setIcon(int icon) {
        this.icon = icon;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
